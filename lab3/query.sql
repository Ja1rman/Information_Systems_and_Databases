SELECT 
    Н_УЧЕНИКИ.ГРУППА, Н_ЛЮДИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, Н_УЧЕНИКИ.СОСТОЯНИЕ, Н_УЧЕНИКИ.ПРИМЕЧАНИЕ
FROM 
    Н_ЛЮДИ
JOIN 
    Н_ОБУЧЕНИЯ
ON 
    Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
JOIN
    Н_УЧЕНИКИ
ON 
    Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
JOIN 
    Н_ПЛАНЫ
ON 
    Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ПЛАН_ИД
JOIN 
    Н_ФОРМЫ_ОБУЧЕНИЯ
ON 
    Н_ПЛАНЫ.ФО_ИД = Н_ФОРМЫ_ОБУЧЕНИЯ.ИД
JOIN 
    Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ
ON 
    Н_ПЛАНЫ.НАПС_ИД = Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.НАПС_ИД
JOIN 
    Н_НАПР_СПЕЦ
ON 
    Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.НС_ИД = Н_НАПР_СПЕЦ.ИД
WHERE 
    Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Заочная'
    AND Н_НАПР_СПЕЦ.КОД_НАПРСПЕЦ LIKE '230101'
    AND Н_УЧЕНИКИ.НАЧАЛО < '2012-09-01';

