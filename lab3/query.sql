SELECT 
    CASE
        WHEN count(*) = 0 THEN 'НЕТ'
        ELSE 'ЕСТь'
	END
FROM 
    Н_ЛЮДИ 
JOIN 
    Н_ОБУЧЕНИЯ 
ON 
    Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
JOIN 
    Н_УЧЕНИКИ
ON 
    Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
WHERE 
    (Н_ЛЮДИ.ОТЧЕСТВО is null OR Н_ЛЮДИ.ОТЧЕСТВО = '.' OR LENGTH(Н_ЛЮДИ.ОТЧЕСТВО) = 0) 
    AND Н_УЧЕНИКИ.ГРУППА LIKE '3102';

