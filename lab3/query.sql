SELECT 
    Н_ЛЮДИ.ИД AS Номер, CONCAT(Н_ЛЮДИ.ФАМИЛИЯ, ' ', Н_ЛЮДИ.ИМЯ, ' ', Н_ЛЮДИ.ОТЧЕСТВО) AS ФИО, AVG(CAST (Н_ВЕДОМОСТИ.ОЦЕНКА AS INTEGER)) AS Ср_оценка
FROM 
    Н_ВЕДОМОСТИ
JOIN 
    Н_ЛЮДИ
ON 
    Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
JOIN 
    Н_ОБУЧЕНИЯ
ON 
    Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
JOIN 
    Н_УЧЕНИКИ
ON 
    Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
WHERE 
    Н_УЧЕНИКИ.ГРУППА LIKE '4100' 
    AND Н_ВЕДОМОСТИ.ОЦЕНКА ~ '^[0-9]+$' 
GROUP BY 
    Н_ЛЮДИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО
HAVING 
    AVG(CAST (Н_ВЕДОМОСТИ.ОЦЕНКА AS INTEGER)) >= (
        SELECT 
            MIN(CAST (Н_ВЕДОМОСТИ.ОЦЕНКА AS INTEGER))
        FROM 
            Н_ВЕДОМОСТИ
        JOIN 
            Н_ЛЮДИ
        ON 
            Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
        JOIN 
            Н_ОБУЧЕНИЯ
        ON 
            Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
        JOIN 
            Н_УЧЕНИКИ
        ON 
            Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
        WHERE 
            Н_ВЕДОМОСТИ.ОЦЕНКА ~ '^[0-9]+$' 
            AND Н_УЧЕНИКИ.ГРУППА LIKE '1101'
    );
-- одна из оценок 99, поэтому больше 5 получился средний

