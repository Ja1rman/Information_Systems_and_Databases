    Н_ВЕДОМОСТИ
ON 
    Н_ТИПЫ_ВЕДОМОСТЕЙ.ИД = Н_ВЕДОМОСТИ.ТВ_ИД
WHERE 
    Н_ТИПЫ_ВЕДОМОСТЕЙ.НАИМЕНОВАНИЕ = 'Ведомость'
    AND Н_ВЕДОМОСТИ.ИД < 1490007
    AND Н_ВЕДОМОСТИ.ИД = 1426978;

