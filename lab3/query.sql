select 
    Н_ГРУППЫ_ПЛАНОВ.ГРУППА
FROM 
    Н_ГРУППЫ_ПЛАНОВ
JOIN 
    Н_ПЛАНЫ
ON 
    Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД = Н_ПЛАНЫ.ПЛАН_ИД
JOIN 
    Н_ОТДЕЛЫ
ON 
    Н_ПЛАНЫ.ОТД_ИД = Н_ОТДЕЛЫ.ОТД_ИД
JOIN 
    Н_УЧЕНИКИ
ON 
    Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД = Н_УЧЕНИКИ.ПЛАН_ИД 
    AND Н_ГРУППЫ_ПЛАНОВ.ГРУППА = Н_УЧЕНИКИ.ГРУППА
WHERE 
    Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ = 'КТиУ'
    AND Н_ПЛАНЫ.КОГДА_СОЗДАЛ BETWEEN '2011-01-01' and '2011-12-31'
GROUP BY 
    Н_ГРУППЫ_ПЛАНОВ.ГРУППА
HAVING 
    COUNT(*) < 5;

