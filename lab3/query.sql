SELECT
    a.ИД AS ИД1,
    a.ИМЯ AS Имя1,
    b.ИД AS ИД2,
    b.ИМЯ AS Имя2
FROM
    Н_ЛЮДИ a
JOIN
    Н_ЛЮДИ b
ON
    a.ИМЯ = b.ИМЯ
    AND a.ИД <> b.ИД;